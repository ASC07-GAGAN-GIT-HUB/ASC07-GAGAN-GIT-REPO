# Example of the listed unicode characters:

# 1. Internationalization
international_string = "こんにちは"  # Japanese for "Hello"
print(international_string) 

# 2. Emojis
emoji_message = "welcome to our store! 😊🚀🌍"  # Smiling face, Rocket, Globe
print(emoji_message)

# 3. Special Symbols
currency_message = "Total cost: €50.00"  # Euro symbol
print(currency_message)
store_name = "Café Mart"  # Accent on 'e'
print(store_name)
